        <p>our primary target audience were <b>gen z students specifically around UCI</b> meaning that reaching them would require more <b>authenticity, personality, transparency, mixed with 
            promises of sustainability. </b>
            <br><br>
            I did a bit of research into the eating habits of Americans as well. These findings helped in <b>concentrating the website's message</b>, creating a 
            sense of <b>urgency</b> to purchase our products for the greater good AND for personal health (best of two worlds)
        </b></p>
        <img src="images/bonsai bento stats.png" alt="">
        <p>my main takeaway was that broke college students must resort to unhealthy sources of nutrition due to time and money constarints (<a href="https://studyfinds.org/college-students-money-in-bank/#:~:text=The%20survey%20of%201%2C000%20U.S.,with%20no%20money%20in%20it.">60% of 
            college students have less than $1k in their pockets</a>) To remedy this, I focused the website on <b>accessibility, ease of use, and speed</b> so that busy students 
            can get the food they need in and out super fast.
            <br><br>
            <b>Our USP:</b> fast yet nutritious and delicious food tailored for university students. This was an idea I carried into the overall
            aesthetic and brand persona of Bonsai Bento.
        </p>
